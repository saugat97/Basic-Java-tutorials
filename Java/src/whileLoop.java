//Let say I want a remainder at 6, and consider the time now as 0
public class whileLoop {
	public static void main(String[] args) {
		int time= 0;
		while (time < 6) {
			
			time++;   //time increments form 0 and won't print until its 6
			
		}
		System.out.println("Its "+ time + " A.M! Wake up.");
	}
}
